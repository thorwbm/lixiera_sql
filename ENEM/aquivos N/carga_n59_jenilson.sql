			--INTO #temp_maior
			  FROM EXT_CORRECOES_REDACAO RED join EXT_projeto_projeto pro on (red.id_projeto = pro.id)
			                                 JOIN EXT_INEP_N02 N02 ON (N02.CO_INSCRICAO = RED.co_inscricao)
                     
			 WHERE cast(red.data_termino as date) = @data  AND
			       red. cancelado = 0 AND RED.id_STATUS = 4 AND 
				   red.id_redacaoouro IS NULL 
				   --AND   datepart (hour, red.data_termino) <= 11
				 --  GROUP BY datepart (hour, red.data_termino)
) 

	,	CTE_USUARIO AS (
			SELECT ID, USERNAME
			  FROM EXT_AUTH_USER  
) 

	,	CTE_SITUACAO AS (
			SELECT ID, SIGLA 
			  FROM EXT_CORRECOES_SITUACAO  
)

	,	CTE_CORRECAO AS (
			SELECT cor.id, cor.data_inicio, cor.data_termino, cor.nota_final,
                   cor.nota_competencia1, cor.nota_competencia2, cor.nota_competencia3, cor.nota_competencia4, cor.nota_competencia5, 
                   tempo_em_correcao = case when COR.TEMPO_EM_CORRECAO > 1200 then 1200 else  COR.TEMPO_EM_CORRECAO end,
				   cor.id_correcao_situacao,
                   cor.id_corretor, cor.id_status, cor.tipo_auditoria_id, cor.id_tipo_correcao, cor.redacao_id, USU.USERNAME AS CPF_CORRETOR, 
				   case when cor.competencia5 = -1 then 1 else 0 end infere_dh, 
				   sit.sigla as sigla_correcao_situacao
			  FROM  EXT_correcoes_correcao cor JOIN CTE_USUARIO USU ON (USU.ID = COR.id_corretor)
			                                   join CTE_SITUACAO sit on (sit.id = cor.id_correcao_situacao)
) 


