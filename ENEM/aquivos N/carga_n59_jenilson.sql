WITH CTE_REDACAO AS (
			SELECT red.id, red.co_barra_redacao, red.co_inscricao, red.link_imagem_recortada, red.link_imagem_original,
			       red.nota_final, red.co_formulario, red.id_prova, red.id_correcao_situacao, red.id_redacao_situacao,
