	,	cte_curriculos as (
			select crc.id as curriculo_id, crc.nome as curriculo_nome, sum(grd.carga_horaria) as carga_horaria 
			from curriculos_curriculo crc join curriculos_grade     gra on (crc.id = gra.curriculo_id)
										  join curriculos_gradedisciplina grd on (gra.id = grd.grade_id)
										  join academico_etapa            eta on (eta.id = gra.etapa_id)
			where crc.nome in ('MEDICINA 2019/12-1 (1-2019)','MEDICINA 2019/12-2 (2-2019)') and 
				  eta.etapa = 1 
			group by  crc.id, crc.nome 
)
	,	cte_cursada as (
			select cur.id as Curriculo_id, aluno.id as Aluno_id, sum(concluida.carga_horaria) as CHCursada
			from curriculos_disciplinaconcluida concluida
			join curriculos_aluno ca on ca.id = concluida.curriculo_aluno_id
			join curriculos_curriculo cur on cur.id = ca.curriculo_id
			join academico_etapaano etapaano on etapaano.id = concluida.etapa_ano_id
			join academico_etapa etapa on etapa.id = etapaano.etapa_id
			join academico_aluno aluno on aluno.id = ca.aluno_id
			where cur.nome in('MEDICINA 2019/12-1 (1-2019)','MEDICINA 2019/12-2 (2-2019)')
			  and etapa.etapa = 1
			  and concluida.status_id in (2,9,10)
			group by cur.id, aluno.id
)
	,	cte_disciplina_aluno as (
			select cra.aluno_id, crc.id as curriculo_id, crc.nome as curriculo_nome,dis.id as disciplina_id, dis.nome as disciplina_nome
			from curriculos_aluno cra join curriculos_curriculo       crc on (crc.id = cra.curriculo_id) 
									  join curriculos_grade           gra on (crc.id = gra.curriculo_id)
									  join curriculos_gradedisciplina grd on (gra.id = grd.grade_id)
									  join academico_etapa            eta on (eta.id = gra.etapa_id)
									  join academico_disciplina       dis on (dis.id = grd.disciplina_id)
			where crc.nome in ('MEDICINA 2019/12-1 (1-2019)','MEDICINA 2019/12-2 (2-2019)') and 
				  eta.etapa = 1 
)
	,	cte_dispensada as (
			select cra.curriculo_id, cra.aluno_id , sum(dsc.carga_horaria) as chcdispensada
		 from curriculos_disciplinaconcluida dsc join curriculos_aluno cra on (cra.id = dsc.curriculo_aluno_id and 
			                                                                                dsc.status_id = 6)
												      join cte_disciplina_aluno dsa on (dsc.disciplina_id = dsa.disciplina_id and 
													                                    cra.aluno_id      = dsa.aluno_id  and
																						cra.curriculo_id  = dsa.curriculo_id)
													  join curriculos_curriculo crc on (crc.id = cra.curriculo_id)
			where crc.nome in ('MEDICINA 2019/12-1 (1-2019)','MEDICINA 2019/12-2 (2-2019)')
	    GROUP BY CRA.curriculo_id, CRA.aluno_id
)


			select crc.curriculo_nome, alu.aluno_nome, alu.aluno_ra, 
			       CUS.CHCursada AS chccursada, 
				   ISNULL(dis.chcdispensada,0) as chcdispensada, 
				   cus.CHCursada + ISNULL(dis.chcdispensada,0) as CARGA_HORARIA_OBTIDA,
			       crc.carga_horaria as CARGA_HORARIA_CURRICULO

			  from cte_alunos alu join cte_cursada cus on (alu.aluno_id = cus.Aluno_id)
			                      join cte_curriculos crc on (crc.curriculo_id = cus.Curriculo_id)
							LEFT  JOIN cte_dispensada DIS ON (DIS.aluno_id = ALU.aluno_id AND 
								                              DIS.curriculo_id = CUS.Curriculo_id)

